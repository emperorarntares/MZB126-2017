function [MEAN,SD] = sample(data)
%Question 1

%SAMPLE calculates the sample mean and sample
%standard deviation of a given vector of data

%Sum of array
N = numel(data);
s = 0;
for i = 1:N
s = s + data(i);
end
%Sample mean
MEAN = s/N;
%The mean of squared differences
diffSquare = 0;
for i = 1:N
a = (MEAN - data(i))^2;
diffSquare = diffSquare + a;
end
%Sample Variance
VAR = (1/(N-1)) * (diffSquare);
%Sample Standard Deviation
SD = sqrt(VAR);
end


function [LOWER UPPER] = CI(data)
%Question 2

%CI calculates a 95% confidence interval 
%of the sample mean data

%Array size
N = numel(data);
%Sample Mean
MEAN = mean(data);
%Sample Variance
VAR = var(data);
%Sample Standard Deviation
SD = sqrt(VAR);
%Time interval
t = tinv(0.975,N-1);
%Lower & Upper bounds
LOWER = MEAN - t*(SD/sqrt(N));
UPPER = MEAN + t*(SD/sqrt(N));
end